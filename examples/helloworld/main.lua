-- retroboy.draw is called every frame. After the call, the screen is updated with any changed pixels.
function retroboy.draw()
	-- Clear the display
	retroboy.display.clear(0)

	-- Calculate the offset for the text.
	offx = 10
	offy = math.floor( 5*math.sin(time) + 20 )

	-- Draw text.
	for j, row in ipairs(hello_world) do
		for i, pixel in ipairs(row) do
			if pixel > 0 then
				retroboy.display.setPixel(offx + i + 1, offy + j + 1, 1)
				retroboy.display.setPixel(offx + i, offy + j, 3)
			end
		end
	end
end
